var result = 0;

function add(number1, number2) {
  result = number1 + number2;
  return result;
}

function substract(number1, number2) {
  result = number1 - number2;
  return result;
}

function multiply(number1, number2) {
  result = number1 * number2;
  return result;
}

function divide(number1, number2) {
  result = number1 / number2;
  return result;
}

function modulo(number1, number2) {
  result = number1 % number2;
  return result;
}
